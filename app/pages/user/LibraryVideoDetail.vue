<script lang="ts">
import { Vue, Component } from 'nuxt-property-decorator';
import LibraryVideoDetail from '@/components/libraryVideoDetail/index.vue';

@Component({
  layout: 'default',
  components: {
    LibraryVideoDetail,
  },
  async fetch({ store, params }) {
    try {
      if (!params.video) {
        console.log('params.video', params);
        await store.dispatch('video/videoDetailById', params._id);
      } else {
        let selectedVideo;
        // @ts-ignore
        if (params.video.isSeries) {
          // @ts-ignore
          selectedVideo = params.video.series[0];
        } else {
          selectedVideo = params.video;
        }
        await store.commit('video/selectedVideo', selectedVideo);
      }
    } catch (e) {
      console.error(e);
    }
  },
})
export default class extends Vue {}
